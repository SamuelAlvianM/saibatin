/**
 * Terjemahkan kode → label bila field dikenal DAN nilainya memang berupa kode.
 * Nilai yang sudah berupa teks (pengajuan baru) dikembalikan apa adanya, jadi
 * fungsi ini aman dipanggil untuk semua data.
 */
export function labelKode(field: string, nilai: string): string | null {
  const grup = FIELD_KE_GRUP[field.replace(/x$/, "")];
  if (!grup) return null;
  if (!/^\d+$/.test(nilai.trim())) return null; // sudah teks — jangan diutak-atik
  return KODE_OPTIONS[grup]?.[nilai.trim()] ?? null;
}
